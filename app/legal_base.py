def get_legal_acts_list(category: str = "trademark") -> list[dict]:
    return LEGAL_ACTS.get(category, [])


def get_concept(concept_id: str) -> dict:
    return CONCEPTS.get(concept_id, {})


def get_all_concepts() -> dict:
    return CONCEPTS


def search_legal_base(query: str) -> list[dict]:
    """Поиск по всей правовой базе."""
    query_lower = query.lower()
    results = []

    for category, acts in LEGAL_ACTS.items():
        # Пропускаем дублирующие категории обратной совместимости
        if category in ("trademark", "copyright"):
            continue
        for act in acts:
            if query_lower in act["title"].lower():
                results.append({"type": "act", "category": category, **act})
            for art in act.get("key_articles", []):
                if query_lower in art["title"].lower() or query_lower in art["content"].lower():
                    results.append({
                        "type": "article",
                        "act_title": act["title"],
                        "category": category,
                        **art,
                    })

    for cid, concept in CONCEPTS.items():
        if query_lower in concept["title"].lower() or query_lower in concept["content"].lower():
            results.append({"type": "concept", "id": cid, **concept})

    return results
